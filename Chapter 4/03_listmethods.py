# Sorts the list
L1 = [1, 8, 7, 2, 21, 15]
L1.sort()
print(L1)

# Reverses the list
L1.reverse()
print(L1)

# Adds at the end of the list
L1.append(45)
print(L1)

# Insert element at the list
L1.insert(2,544)
print(L1)

# Removes element at the index
L1.pop(2)
print(L1)

#Remove the no.
L1.remove(21)
print(L1)